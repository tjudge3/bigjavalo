public class HelloPrinter { //This is the main class (the blue print)
    public static void main (String[] args) //This is the main method
    {//Below are the instructions inside the method
        System.out.println("Hello World!"); //Print - Echo - exactly that
        System.out.println(3 + 4); //Do the calculation and print the results
        System.out.print(0); //Print the integer without line break
        System.out.print(7); //Print the integer without line break
        System.out.println("This"); //Carriage return after
        System.out.printf("'%15s' %n", "New"); //Printing with formatting, functionally same as print otherwise
        System.out.print("York"); //No line breakage
    }
}
